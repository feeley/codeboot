samples = [
    (0, 1, 1),
    (0, 1, 2),
    (3, 1, -1),
    (0, 10, 6),
    (0, 9, 6),
    (-1000, 1000, 3),
    (-1000, 1001, 3),
    (0, -1, 1),
    (0, -5, 7)
]

for s in samples:
    for t in samples:
        r1 = range(s[0], s[1], s[2])
        r2 = range(t[0], t[1], t[2])
        print(r1, "==", r2, ":", r1 == r2)